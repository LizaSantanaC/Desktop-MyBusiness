    private void tbUsuariosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbUsuariosKeyReleased
        // TODO add your handling code here:
        txtNombre.setText(String.valueOf(tbUsuarios.getValueAt(tbUsuarios.getSelectedRow(),0)));
        try {
            u = uDAO.consultaAlumno(txtNombre.getText());
        } catch (Exception ex) {
            Logger.getLogger(VentanaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtClave.setText(u.getContraseña());
        txtClaveConf.setText(u.getContraseña());
        ckAdmin.setSelected(u.isIsAdmin());
        
         r.habilitarComponentes(false, txtNombre, txtClave, txtClaveConf);
    }//GEN-LAST:event_tbUsuariosKeyReleased

