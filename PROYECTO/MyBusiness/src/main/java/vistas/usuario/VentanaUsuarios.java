    private void txtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtGuardarActionPerformed
        // TODO add your handling code here:
        if (r.componentesVacios(txtNombre, txtClave, txtClaveConf)) {
            r.mostrarNotificacionError("Falta informacion", "Debes ingresar todos los datos del usuario...");
        } else {

            if (txtClave.getText().equals(txtClaveConf.getText())) {
                u.setUsuario(txtNombre.getText());
                u.setIsAdmin(ckAdmin.isSelected());
                u.setContraseña(txtClaveConf.getText());

                try {
                    if (uDAO.consultaAlumno(u.getUsuario()) != null) {
                        r.mostrarNotificacionCuidado("Usuario no registrado", "El usuario ya se encuentra registrado.");
                    } else {
                        if (uDAO.registrarUsuario(u)) {
                            r.mostrarNotificacionExito("Operacion exitosa !!!", "Usuario registrado con exito.");
                            r.mostrarTabla(tbUsuarios, "SELECT Nombre,Administrador FROM Usuarios");
                            r.limpiar(txtNombre, txtClave, txtClaveConf, txtBuscar);
                            ckAdmin.setSelected(false);
                            tbUsuarios.clearSelection();
                            r.habilitarComponentes(false, txtNombre, txtClave, txtClaveConf, txtGuardar, txtBuscar);
                            r.limpiar(txtNombre, txtClave, txtClaveConf);
                            ckAdmin.setSelected(false);
                            tbUsuarios.clearSelection();
                        } else {
                            r.mostrarNotificacionError("Error al registrar usuario ", "No se pudo registrar al usuario.");
                        }
                    }
                } catch (Exception ex) {
                    Logger.getLogger(VentanaUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                r.mostrarNotificacionError("Contaseña incorrecta ", "La contaseña de confirmacion no coincide");
            }

        }
    }//GEN-LAST:event_txtGuardarActionPerformed

