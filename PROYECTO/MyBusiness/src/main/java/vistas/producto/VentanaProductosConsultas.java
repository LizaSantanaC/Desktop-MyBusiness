    private void btnProductosBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductosBuscarActionPerformed
        // TODO add your handling code here:
        if (rbCodigo.isSelected()) {

            r.mostrarTabla(tbProductos, "SELECT * FROM Productos WHERE Codigo LIKE '" + txtProductoCodigo.getText() + "%'");
            if (tbProductos.getRowCount() == 0) {
                r.mostrarNotificacionCuidado("Sin resultados", "No se a encontrado ningun resultado... ");
            }

        } else if (rbNombre.isSelected()) {
            r.mostrarTabla(tbProductos, "SELECT * FROM Productos WHERE Nombre LIKE '" + txtProductoNombre.getText() + "%'");
            if (tbProductos.getRowCount() == 0) {
                r.mostrarNotificacionCuidado("Sin resultados", "No se a encontrado ningun resultado... ");
            }

        } else if (rbMarca.isSelected()) {
            r.mostrarTabla(tbProductos, "SELECT * FROM Productos WHERE Marca LIKE '" + txtProductoMarca.getText() + "%'");
            if (tbProductos.getRowCount() == 0) {
                r.mostrarNotificacionCuidado("Sin resultados", "No se a encontrado ningun resultado... ");
            }

        } else if (rbPrecioCompra.isSelected()) {
            r.mostrarTabla(tbProductos, "SELECT * FROM Productos WHERE Precio_Compra LIKE '" + txtProductoPrecioCompra.getText() + "%'");
            if (tbProductos.getRowCount() == 0) {
                r.mostrarNotificacionCuidado("Sin resultados", "No se a encontrado ningun resultado... ");
            }

        } else if (rbPrecioVenta.isSelected()) {
            r.mostrarTabla(tbProductos, "SELECT * FROM Productos WHERE Precio_Venta LIKE '" + txtProductoPrecioVenta.getText() + "%'");

            if (tbProductos.getRowCount() == 0) {
                r.mostrarNotificacionCuidado("Sin resultados", "No se a encontrado ningun resultado... ");
            }

        } else if (rbProveedor.isSelected()) {
            r.mostrarTabla(tbProductos, "SELECT * FROM Productos WHERE Proveedor LIKE '" + cbProductoProveedor.getSelectedItem() + "%'");

            if (tbProductos.getRowCount() == 0) {
                r.mostrarNotificacionCuidado("Sin resultados", "No se a encontrado ningun resultado... ");
            }

        } else if (rbAmacen.isSelected()) {
            r.mostrarTabla(tbProductos, "SELECT * FROM Productos WHERE Num_Almacen LIKE '" + spStock.getValue() + "%'");

            if (tbProductos.getRowCount() == 0) {
                r.mostrarNotificacionCuidado("Sin resultados", "No se a encontrado ningun resultado... ");
            }

        }
    }//GEN-LAST:event_btnProductosBuscarActionPerformed

